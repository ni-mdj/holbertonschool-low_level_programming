#include <stdio.h>
#include <stdlib.h>

/**
 * main - Entry point of the program
 *
 * Description: Prints a quote using the puts function
 * Return: Always 0 (Success)
 */
int main(void)
{

	for (char alphabet = 'a'; alphabet <= 'z'; alphabet++)
{
	putchar(alphabet);
}
	putchar('\n');

return (0);
}
